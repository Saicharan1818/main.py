#DATA TYPES
#str
name = "GenAI Journey"
print(name)
print(type(name))

#float
b = 3.14
print(b)
print(type(b))

#int
a = 10
print(a)
print(type(a))

#complex
c = 2 + 3j
print(c)
print(type(c))

#list
numbers = [1, 2, 3, 4]
print(numbers)
print(type(numbers))

#tuple
tup = (10, 20)
print(tup)
print(type(tup))

#dictionary
student = {"name": "Sai", "age": 19}
print(student)
print(type(student))

#boolean
bool = True
print(bool)
print(type(bool))

#set
unique_nums = {1, 2, 3, 3}
print(unique_nums)
print(type(unique_nums))

#FROZENTYPE

frozen = frozenset([1, 2, 3])
print(frozen)
print(type(frozen))


#BYTES 
byte_data = b"Hello"
print(byte_data)
print(type(byte_data))



#variables 
x = 10
y = 5
sum = x + y

print("Sum:", sum)  # Outputs: 15
diff = x - y
print("Difference:", diff)  # Outputs: 5






